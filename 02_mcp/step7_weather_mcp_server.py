# ======================================================================
# 第 1 部分:导入 FastMCP
# ======================================================================

# 【Python 基础:from A import B】
#   从 A 这个模块里,把名字叫 B 的东西拿过来用。
#   这里 A = mcp.server.fastmcp(MCP 官方库里的一个位置),B = FastMCP。
#
# 【MCP 概念:FastMCP 是什么】
#   FastMCP 是 MCP 官方库给的一个"类"(class)。
#   类 = 一张"图纸";等下用 FastMCP(...) 就能照图纸造出一个真正的服务端对象。
#   它帮你把 MCP 协议那些繁琐的底层细节都封装好了,你只管挂工具。
# 【联网查天气要用的标准库 —— step4 用过的老朋友】
import urllib.request   # 发 HTTP 请求(真正联网)
import urllib.parse     # 把"北京"这种中文安全地塞进网址里

from mcp.server.fastmcp import FastMCP


# ======================================================================
# 第 2 部分:照图纸,造一个服务端对象
# ======================================================================

# 【Python 基础:类与对象】
#   FastMCP        = 图纸(类)
#   FastMCP(...)   = 照图纸造出一个真实的东西(对象)
#   mcp_server     = 给这个造出来的服务端对象起的名字
#
# 【MCP 概念:name 是"服务"的名字,不是"工具"的名字】
#   name="weather-server" 是这个【服务端】的标识(像店名)。
#   真正的工具名,是下面那个函数 get_weather(像店里卖的菜)。
mcp_server = FastMCP(name="weather-server")
# ======================================================================
# 第 3 部分:定义一个工具,并"挂"到服务端上
# ======================================================================

# 【Python 基础:装饰器(decorator)是什么?】
#   装饰器,就是写在函数【正上方】、以 @ 开头的那一行。
#   你可以把它理解成:给下面这个函数"别上一个工牌 / 贴一张便利贴"。
#   函数本身的代码一个字没变,但贴上之后,某个系统就"认识"它、把它登记在册了。
#
#   这里的 @mcp_server.tool() 这张便利贴写着:
#       "嘿 mcp_server,把我下面这个函数,登记进你的工具菜单。"
#
#   没有这一行 → get_weather 只是个普通函数,MCP 客户端看不见它。
#   加上这一行 → 它就成了一个【对外公开的 MCP 工具】。
@mcp_server.tool()
def get_weather(city: str) -> str:
    """查询指定城市的当前天气。"""
    # 【MCP 概念:FastMCP 会"读"你的函数,自动生成那坨工具说明书】★重点★
    #
    #   看这个函数头一行:  def get_weather(city: str) -> str:
    #     · 函数名 get_weather    → 自动变成 工具的【名字】
    #     · 参数  city: str       → 自动变成 工具的【参数表】(名叫 city,类型字符串,必填)
    #     · 上面三引号的 docstring → 自动变成 工具的【说明】(模型靠它判断"啥时候该用这工具")
    #
    #   也就是说,step5 里你【手写】的这一大坨,现在 FastMCP 全自动替你生成:
    #       {
    #         "name": "get_weather",
    #         "description": "查询指定城市的当前天气。",
    #         "parameters": {
    #           "type": "object",
    #           "properties": {"city": {"type": "string"}},
    #           "required": ["city"]
    #         }
    #       }
    #   你只需要把"函数名、类型标注、docstring"这三样写好,剩下的它包了。
    #
    # 【小提醒:docstring 要简洁,教学笔记请写在 # 注释里】
    #   docstring(三引号)会被模型看到,占模型的"阅读量",所以一句话说清用途就行。
    #   像现在这些长篇大论的讲解,都放在 # 注释里——# 注释只给人看,不会进 MCP、不进模型。

    # ★ 真·联网查天气(这段你在 step4 见过,这次原样搬进 MCP 工具里)。
    #   关键体验:换成真查天气,我们【只动 step7 这一个文件】,
    #   step8 客户端碰都不碰 —— 这就是 MCP "工具和使用方分离"的爽点。
    try:
        request_url = f"https://wttr.in/{urllib.parse.quote(city)}?format=%C+%t&lang=zh"
        request = urllib.request.Request(request_url, headers={"User-Agent": "curl/8.0"})
        with urllib.request.urlopen(request, timeout=10) as response:
            weather_text = response.read().decode("utf-8").strip()
        weather_result = f"{city}当前天气:{weather_text}"
    except Exception as error:
        # 联网可能失败(断网、超时、城市查不到):优雅报错,别让服务端崩。
        weather_result = f"查询 {city} 天气失败:{error}"

    # 【Python 基础:return vs print】
    #   return = 把结果【交还给调用它的人】(这里是交还给 MCP 客户端)
    #   print  = 把文字打到屏幕上给人看
    #   这里必须 return,因为结果要顺着 MCP 通道传回客户端。
    return weather_result
@mcp_server.tool()
def get_peopletotal(city: str) -> str:
    """查询指定城市的人口数量。"""
    peopletotal_result = f"{city}1000万(这是 step7 的 MCP 测试数据,还没真查)"
    return peopletotal_result
# 想再加一个工具?就再写一个函数 + 再贴一行 @mcp_server.tool(),就这么简单。
# (这就是 step5 里那个"注册表"的升级版 —— 现在贴个标签就自动注册了。)


# ======================================================================
# 第 4 部分:启动服务端
# ======================================================================

# 【Python 基础:if __name__ == "__main__"】
#   · 直接运行这个文件(python step7_...py)→ __name__ 是 "__main__" → 条件成立 → 启动服务
#   · 别的文件只是 import 它(step8 就会这么干)→ 条件不成立 → 【不会】自动启动
#   这行就是个开关:决定"什么时候才真的把服务跑起来"。
if __name__ == "__main__":
    # 【MCP 概念:stdio 是什么?为什么跑起来"像卡住了"?】
    #
    #   transport = "用什么通道收发消息"。stdio 是最简单的一种。
    #   stdio = standard input / output(标准输入/输出),就是程序最普通的那对"读写口"。
    #     平时:你终端打字 = 写进程序的标准输入;程序 print = 写到标准输出。
    #     这里:MCP 把这对读写口【借来当客户端↔服务端的传话筒】:
    #           客户端写一句 → 进服务端的标准输入
    #           服务端答一句 → 从标准输出出去 → 客户端读到
    #
    #   ★ 所以你【单独】运行这个文件,屏幕会一片空白、光标在闪、像卡死 —— 这是【正常的】!
    #     它没死机,是在【安静地等客户端来连它说话】。
    #     没报错 + 一直停着 = 启动成功。想停掉,按 Ctrl + C。
    #
    #   ★ 一条铁律:stdio 模式下,【不要】在服务端代码里随便 print()!
    #     因为标准输出现在是"协议传话筒",你乱 print 会污染通信,把客户端搞晕。
    #     (真正的验证,交给下一步 step8 的客户端来做。)
    mcp_server.run(transport="stdio")
