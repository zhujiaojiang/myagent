import asyncio   # 【Python 基础】asyncio = Python 自带的"异步"工具箱,不用额外安装
import time      # 用来计时,好让你亲眼"看见"异步到底快在哪


# ======================================================================
# 先说人话:同步 vs 异步,到底差在哪?
# ----------------------------------------------------------------------
# 【同步】= 你前面 step1~step6 写的代码,全是同步的:
#       一件事没干完,绝不碰下一件。
#       烧水要 3 秒,你就傻站在水壶前干等 3 秒,水开了才去烤面包。
#       总共 3 + 2 = 5 秒。
#
# 【异步】= 烧水要"等",但等的时候我人是空的,可以先去把面包放进烤箱。
#       两件事的"等待"重叠在一起。总共只花 max(3, 2) = 3 秒。
#
# await 这个词,就是在说:
#       "这件事要等一会儿(慢操作),我在这儿等结果,
#        但我不死占着,允许程序这会儿先去干别的。"
# ======================================================================


# 【Python 基础:async def】
# 普通函数是  def 函数名():
# 前面加 async,就变成"协程函数"——一种"可以中途暂停、去等慢操作"的特殊函数。
# 只有 async def 函数内部,才允许使用 await。
async def boil_water():
    print("  [水壶] 开始烧水,需要 3 秒……")

    # 【关键】asyncio.sleep(3) 用来模拟"烧水花 3 秒"这种慢操作。
    # await 的意思:在这儿等 3 秒,但这 3 秒【不挡着别人干活】。
    # 注意区别:
    #   time.sleep(3)      = 真的卡死整个程序 3 秒(同步、霸道)
    #   asyncio.sleep(3)   = 礼貌地等 3 秒,期间让出舞台给别人(异步)
    await asyncio.sleep(3)

    print("  [水壶] 水烧开了!")
    return "一壶热水"


async def toast_bread():
    print("  [烤箱] 开始烤面包,需要 2 秒……")
    await asyncio.sleep(2)
    print("  [烤箱] 面包烤好了!")
    return "两片烤面包"


# 【Python 基础:在 async 函数里调用别的 async 函数】
async def make_breakfast():
    start_time = time.time()   # 记下开始的时刻

    # 【核心:asyncio.gather】
    # gather = "把这几件异步的事【同时】开干,然后一起等它们都完成"。
    # 因为"烧水的 3 秒"和"烤面包的 2 秒"是【重叠】着等的,
    # 所以总共≈3 秒,而不是 5 秒。这就是异步省时间的地方。
    hot_water, toast = await asyncio.gather(
        boil_water(),
        toast_bread(),
    )

    seconds_used = time.time() - start_time
    print(f"\n早餐齐活:{hot_water} + {toast}")
    print(f"总共只花了 {seconds_used:.1f} 秒(换成同步写法要花 5 秒!)")


# ======================================================================
# 【Python 基础:asyncio.run —— 异步世界的"总开关/启动按钮"】
# ----------------------------------------------------------------------
# async def 定义出来的函数,你【不能】像普通函数那样直接 make_breakfast() 就运行,
# 那样只会得到一个"协程对象",并不会真的执行。
# 必须用 asyncio.run(...) 来启动:它会开一个"事件循环",
# 把整段异步代码跑完,再自动关掉。
# 把它理解成"异步程序的总开关",一个程序通常只按一次。
# ======================================================================
if __name__ == "__main__":
    asyncio.run(make_breakfast())
